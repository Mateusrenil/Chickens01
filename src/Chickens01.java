public class Chickens01 {

    public static void main(String[] args) {
        int count=15;   // contagem inicial de ovos
        int a, b, c;    // segunda terça e quarta
        a = count;          //contagem de ovos de segunda com três galinhas
        b = count + 5;      //contagem de ovos de terça com mais uma galinha
        c = count - 5;      //contagem de ovos de quarta com menos duas galinhas
        
        System.out.println("Segunda feira = " + a + " ");
        System.out.println("Terça feira = " + b + " , ");
        System.out.println("Quarta feira =" + c + ", " );
        // segunda = 15 ovos | terça = 20 ovos | quarta = 10 ovos
        
        
        
        
       
   
        
        
        
        
    }   
}
